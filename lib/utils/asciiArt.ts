/**
 * Converts text to ASCII art
 * @param text - The text to convert
 * @param options - ASCII art options
 * @returns The generated ASCII art as a string
 */
export function textToAscii(text: string, options: AsciiOptions = {}): string {
  const {
    font = 'standard',
    frame = 'none',
    color = 'none',
    align = 'left',
    letterSpacing = 1,
    width
  } = options;
  
  const fontMap = fonts[font];
  if (!fontMap) {
    throw new Error(`Unknown font: ${font}`);
  }
  
  const upperText = text.toUpperCase();
  const lines: string[] = [];
  
  // Determine the height of characters in the font
  const charHeight = Object.values(fontMap)[0]?.length || 5;
  
  // Build lines character by character
  for (let lineIdx = 0; lineIdx < charHeight; lineIdx++) {
    let line = '';
    for (let charIdx = 0; charIdx < upperText.length; charIdx++) {
      const char = upperText[charIdx];
      const charArt = fontMap[char] || fontMap[' '] || ['     '];
      
      if (charArt[lineIdx]) {
        line += charArt[lineIdx];
      }
      
      // Add letter spacing (except after the last character)
      if (charIdx < upperText.length - 1) {
        line += ' '.repeat(letterSpacing);
      }
    }
    lines.push(line);
  }
  
  // Apply frame
  const framedLines = applyFrame(lines, frame);
  
  // Apply alignment and width
  const targetWidth = width || Math.max(...framedLines.map(l => l.length));
  const alignedLines = framedLines.map(l => alignLine(l, targetWidth, align));
  
  // Apply color
  const coloredLines = alignedLines.map((line, idx) => 
    colorFunctions[color](line, idx, alignedLines.length)
  );
  
  return coloredLines.join('\n');
}

