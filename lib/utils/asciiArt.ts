function getCharWidth(char: string[], letterSpacing: number): number {
  if (char.length === 0) return 0;
  return char[0].length + letterSpacing;
}

function alignLine(line: string, width: number, align: AlignStyle): string {
  const lineWidth = line.length;
  if (lineWidth >= width) return line;
  
  switch (align) {
    case 'left':
      return line.padEnd(width, ' ');
    case 'right':
      return line.padStart(width, ' ');
    case 'center':
      const padding = Math.floor((width - lineWidth) / 2);
      return ' '.repeat(padding) + line + ' '.repeat(width - lineWidth - padding);
    default:
      return line;
  }
}

function applyFrame(lines: string[], frame: FrameStyle): string[] {
  if (frame === 'none') return lines;
  
  const f = frames[frame];
  const maxWidth = Math.max(...lines.map(l => l.length));
  const result: string[] = [];
  
  // Top border
  result.push(f.cornerTL + f.top.repeat(maxWidth) + f.cornerTR);
  
  // Content lines
  for (const line of lines) {
    result.push(f.left + line.padEnd(maxWidth, ' ') + f.right);
  }
  
  // Bottom border
  result.push(f.cornerBL + f.bottom.repeat(maxWidth) + f.cornerBR);
  
  return result;
}

