/**
 * ASCII Art Generator
 * A utility for converting text to ASCII art using various font styles
 */

// ============================================================================
// TYPES
// ============================================================================

export type FontStyle = 
  | 'standard' 
  | 'shadow' 
  | 'slant' 
  | 'big-money' 
  | 'small' 
  | 'block' 
  | 'graffiti' 
  | 'ice' 
  | 'bold' 
  | 'tiny'
  | 'alpha';

export type FrameStyle =
  | 'none'
  | 'single'
  | 'double'
  | 'thick'
  | 'shadow'
  | 'rounded';

export type ColorStyle =
  | 'none'
  | 'rainbow'
  | 'neon'
  | 'gold'
  | 'ice'
  | 'fire'
  | 'matrix'
  | 'ocean';

export type AlignStyle = 'left' | 'center' | 'right';

export interface AsciiOptions {
  font?: FontStyle;
  frame?: FrameStyle;
  color?: ColorStyle;
  align?: AlignStyle;
  letterSpacing?: number;
  width?: number;
}

